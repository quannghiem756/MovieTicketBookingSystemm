// Movie service
const Movie = require('../domain/Movie');
const VectorSyncService = require('../infrastructure/VectorSyncService');

class MovieService {
  constructor(movieRepository) {
    this.movieRepository = movieRepository;
  }

  async createMovie(movieData) {
    const movie = new Movie(
      null, // ID will be generated by database
      movieData.title,
      movieData.director,
      movieData.cast,
      movieData.synopsis,
      movieData.duration,
      movieData.genre,
      movieData.rating,
      movieData.posterUrl || '', // Use the uploaded file path or empty string
      movieData.trailerUrl,
      movieData.releaseDate,
      movieData.endDate,
      movieData.formats || ['2D']
    );

    const createdMovie = await this.movieRepository.create(movie);
    
    // Sync with vector service (async, don't await if we don't want to block, 
    // but here we might want to ensure it's triggered)
    if (createdMovie) {
      VectorSyncService.syncMovieUpsert(createdMovie);
    }
    
    return createdMovie;
  }

  async getMovieById(id) {
    return await this.movieRepository.findById(id);
  }

  async getAllMovies(page, limit, search, format) {
    return await this.movieRepository.findAll(page, limit, search, format);
  }

  async getNowShowing(page, limit, format) {
    return await this.movieRepository.findNowShowing(page, limit, format);
  }

  async getComingSoon(page, limit, format) {
    return await this.movieRepository.findComingSoon(page, limit, format);
  }

  async updateMovie(id, movieData) {
    const movie = new Movie(
      id,
      movieData.title,
      movieData.director,
      movieData.cast,
      movieData.synopsis,
      movieData.duration,
      movieData.genre,
      movieData.rating,
      movieData.posterUrl || '', // Use the uploaded file path or empty string
      movieData.trailerUrl,
      movieData.releaseDate,
      movieData.endDate,
      movieData.formats
    );

    const updatedMovie = await this.movieRepository.update(id, movie);
    
    if (updatedMovie) {
      VectorSyncService.syncMovieUpsert(updatedMovie);
    }
    
    return updatedMovie;
  }

  async deleteMovie(id) {
    const result = await this.movieRepository.delete(id);
    
    if (result) {
      VectorSyncService.syncMovieDelete(id);
    }
    
    return result;
  }
}

module.exports = MovieService;