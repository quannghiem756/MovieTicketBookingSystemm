// Movie service
const Movie = require('../domain/Movie');

class MovieService {
  constructor(movieRepository) {
    this.movieRepository = movieRepository;
  }

  async createMovie(movieData) {
    const movie = new Movie(
      null, // ID will be generated by database
      movieData.title,
      movieData.director,
      movieData.cast,
      movieData.synopsis,
      movieData.duration,
      movieData.genre,
      movieData.rating,
      movieData.posterUrl,
      movieData.trailerUrl,
      movieData.releaseDate,
      movieData.endDate
    );
    
    return await this.movieRepository.create(movie);
  }

  async getMovieById(id) {
    return await this.movieRepository.findById(id);
  }

  async getAllMovies(page, limit) {
    return await this.movieRepository.findAll(page, limit);
  }

  async getNowShowing() {
    return await this.movieRepository.findNowShowing();
  }

  async getComingSoon() {
    return await this.movieRepository.findComingSoon();
  }

  async updateMovie(id, movieData) {
    const movie = new Movie(
      id,
      movieData.title,
      movieData.director,
      movieData.cast,
      movieData.synopsis,
      movieData.duration,
      movieData.genre,
      movieData.rating,
      movieData.posterUrl,
      movieData.trailerUrl,
      movieData.releaseDate,
      movieData.endDate
    );
    
    return await this.movieRepository.update(id, movie);
  }

  async deleteMovie(id) {
    return await this.movieRepository.delete(id);
  }
}

module.exports = MovieService;